// Normal Function

function hello(name) {
  console.log(`Hello Normal, ${name}`);
}
hello("Adil BAYRAKTAR"); // Output: Hello Normal, Adil BAYRAKTAR

// Arrow Function

const helloArrow = (name) => console.log(`Hello From Arrow, ${name}`);

helloArrow("Adil BAYRAKTAR"); // Output: Hello From Arrow, Adil BAYRAKTAR
